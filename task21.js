// Add 7 to the input number and return the result.
function addSeven(num) {
    return num + 7;
}
console.log(addSeven(3));   // Output: 10
console.log(addSeven(-4));  // Output: 3

//Divide the input number by 2 and return the result.
function divideByTwo(num) {
    return num / 2;
}
console.log(divideByTwo(10));  // Output: 5
console.log(divideByTwo(7));   // Output: 3.5
